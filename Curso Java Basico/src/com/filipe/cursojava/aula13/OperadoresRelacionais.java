package com.filipe.cursojava.aula13;

public class OperadoresRelacionais {

    public static void main(String[] args) {

        int valor1 = 1;
        int valor2 = 2;

        System.out.println("valor1 == valor2 " + (valor1 == valor2)); //Igual
        System.out.println("valor1 != valor2 " + (valor1 != valor2)); //Diferente
        System.out.println("valor1 > valor2 " + (valor1 > valor2)); //Menor que
        System.out.println("valor1 >= valor2 " + (valor1 >= valor2)); //Menor igual que
        System.out.println("valor1 < valor2 " + (valor1 < valor2));// Maior que
        System.out.println("valor1 <= valor2 " + (valor1 <= valor2));//Maior igual que

    }
}
